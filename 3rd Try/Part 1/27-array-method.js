// array  push pop

// array shift unshift

let fruits = ["apple ", "mango", "grapes"]
console.log(fruits)

// push

fruits.push("banana")
console.log(fruits)

// pop -> remove the item from the last of the array
console.log(fruits.pop())

console.log(fruits)

// unshift -> add items at the start

fruits.unshift("orange")
console.log(fruits)

// shift - it will remove item from start

console.log(fruits.shift())
console.log(fruits)


// push and pop is fast as compared to shift and unshift

