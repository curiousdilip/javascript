// array push pop // faster

// array shift unshift //slower than push pop

// array is mutable can be changes using array methods

let fruits = ["apple", "mango", "grapes"];
console.log(fruits);

fruits.push("dilip"); // add element in the last

console.log(fruits);

fruits.pop(); //removes last element
console.log(fruits);

fruits.unshift("banana"); // add element in the first

console.log(fruits);

fruits.shift(); //removes first element
console.log(fruits);

fruits.unshift("banana", "naspatti");
console.log(fruits);
